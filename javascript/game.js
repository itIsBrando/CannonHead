		static getRandomMap() {
			return Math.floor(Math.random() * maps.length);
		}
		

    fullRedraw() {
        context.fillStyle = "#000000";
        context.fillRect(0, 0, 128, 128);
        
        this.draw();

        Player.drawAll();

        Bomb.getAll().forEach(b => {
            b.draw();
        })

        drawDigit(this.scores[0], 30, 8);
        drawDigit(this.scores[1], 128 - 30, 8);
    }

    // called every frame
    update() {
        if(game.breakUpdate == true) {
            game.breakUpdate = false;
            return;
        }

        Bomb.clearAll();

        Player.clearAll();
        
        Bomb.getAll().forEach(b => {
            b.move();
        });
        
        players.forEach(p => {
            p.move(0);
            p.fall();
            if(p.isCPU) { p.doCPU(); }
        });
        
        Player.drawAll();
        Bomb.drawAll();

        window.requestAnimationFrame(game.update);
    }
    
    // resets game after dying
    reset() {
        this.breakUpdate = true;
        gameState.bombs.splice(0, gameState.bombs.length);
        
        players.forEach(p => {
            p.state = p.strength = 0;
        });
        
        this.reloadMap();
        this.resetPlayers();
        this.fullRedraw();
        
        if(this.scores[0] >= gameState.playUntil) {
            this.gameOver(0);
        } else if(this.scores[1] >= gameState.playUntil) {
            this.gameOver(1);
        } else {
            this.update();
        }
        
        Player.clearAll();

    }
    
    // lose = player num who died
    gameOver(lose) {
        // stop the update loop from running
        this.breakUpdate = true;
        inGame = false;

        // draw game ending
        context.fillStyle = "#FFFFFF";
        context.font = "8px Arial";
        if(lose == 0) {
            context.fillText("You Lose", 0, 30);
        } else {
            context.fillText("You Win", 0, 30);
        }
    }
    
